; ------------------------------------------------------------------------------
; VSync
; ------------------------------------------------------------------------------

ol_VSync:
	st.b	ol_vsync_flag.w					; Set VSync flag
	move.w	#$2000,sr					; Enable interrupts

.Wait:
	tst.b	ol_vsync_flag.w					; Has the V-BLANK interrupt run yet?
	bne.s	.Wait						; If not, branch
	rts


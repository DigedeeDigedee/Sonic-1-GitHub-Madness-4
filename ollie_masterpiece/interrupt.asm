; VSync
; ------------------------------------------------------------------------------

ol_VSync:
	st.b	v_vbla_routine.w				; Set VSync flag
	enable_ints						; Enable interrupts

.Wait:
	tst.b	v_vbla_routine.w				; Has the V-BLANK interrupt run yet?
	bne.s	.Wait						; If not, branch
	rts

